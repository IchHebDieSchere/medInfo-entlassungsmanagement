export const DISCHARGE_STATUS = Object.freeze({
  RECEIVED: 'RECEIVED',
  VALIDATED: 'VALIDATED',
  PATIENT_SYNCHRONIZED: 'PATIENT_SYNCHRONIZED',
  ENCOUNTER_VALIDATED: 'ENCOUNTER_VALIDATED',
  ENCOUNTER_CLOSED: 'ENCOUNTER_CLOSED',
  COMPOSITION_CREATED: 'COMPOSITION_CREATED',
  DOCUMENT_REFERENCE_CREATED: 'DOCUMENT_REFERENCE_CREATED',
  FHIR_AUDIT_RECORDED: 'FHIR_AUDIT_RECORDED',
  COMPLETED: 'COMPLETED',
  FAILED: 'FAILED'
})

export const ALLOWED_DISCHARGE_TRANSITIONS = Object.freeze({
  [DISCHARGE_STATUS.RECEIVED]: [
    DISCHARGE_STATUS.VALIDATED,
    DISCHARGE_STATUS.FAILED
  ],

  [DISCHARGE_STATUS.VALIDATED]: [
    DISCHARGE_STATUS.PATIENT_SYNCHRONIZED,
    DISCHARGE_STATUS.FAILED
  ],

  [DISCHARGE_STATUS.PATIENT_SYNCHRONIZED]: [
    DISCHARGE_STATUS.ENCOUNTER_VALIDATED,
    DISCHARGE_STATUS.FAILED
  ],

  [DISCHARGE_STATUS.ENCOUNTER_VALIDATED]: [
    DISCHARGE_STATUS.ENCOUNTER_CLOSED,
    DISCHARGE_STATUS.FAILED
  ],

  [DISCHARGE_STATUS.ENCOUNTER_CLOSED]: [
    DISCHARGE_STATUS.COMPOSITION_CREATED,
    DISCHARGE_STATUS.FAILED
  ],

  [DISCHARGE_STATUS.COMPOSITION_CREATED]: [
    DISCHARGE_STATUS.DOCUMENT_REFERENCE_CREATED,
    DISCHARGE_STATUS.FAILED
  ],

  [DISCHARGE_STATUS.DOCUMENT_REFERENCE_CREATED]: [
    DISCHARGE_STATUS.FHIR_AUDIT_RECORDED,
    DISCHARGE_STATUS.FAILED
  ],

  [DISCHARGE_STATUS.FHIR_AUDIT_RECORDED]: [
    DISCHARGE_STATUS.COMPLETED,
    DISCHARGE_STATUS.FAILED
  ],

  [DISCHARGE_STATUS.COMPLETED]: [],
  [DISCHARGE_STATUS.FAILED]: []
})

export function canTransitionDischarge(currentStatus, nextStatus) {
  const allowedNextStatuses = ALLOWED_DISCHARGE_TRANSITIONS[currentStatus]

  if (!allowedNextStatuses) {
    return false
  }

  return allowedNextStatuses.includes(nextStatus)
}
